# Integer: whole number, no decimal
age = 25
print("Age:", age)           # Output: Age: 25
print("Type of age:", type(age))  # Output: <class 'int'>

# Float: number with decimal
height = 5.9
print("Height:", height)         # Output: Height: 5.9
print("Type of height:", type(height))  # Output: <class 'float'>

# String: text enclosed in quotes
name = "Vikash"
print("Name:", name)         # Output: Name: Alice
print("Type of name:", type(name))  # Output: <class 'str'>

# Boolean: True or False
student = True
print("Is student:", student)     # Output: Is student: True
print("Type of is_student:", type(student))  # Output: <class 'bool'>

# Dynamic Typing: Python allows variables to change type
x = 10            # x is an integer
print("x:", x, "Type:", type(x))  # Output: x: 10 Type: <class 'int'>

x = "Ten"         # Now x is a string
print("x:", x, "Type:", type(x))  # Output: x: Ten Type: <class 'str'>

x = 3.14          # Now x is a float
print("x:", x, "Type:", type(x))  # Output: x: 3.14 Type: <class 'float'>
